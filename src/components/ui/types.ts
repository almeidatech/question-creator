/**
 * Shared Types for UI Components
 */

/**
 * Common size variants
 */
export type SizeVariant = 'sm' | 'md' | 'lg';

/**
 * Common color/status variants
 */
export type ColorVariant =
  | 'primary'
  | 'secondary'
  | 'success'
  | 'error'
  | 'warning'
  | 'info'
  | 'neutral';

/**
 * Form input variants
 */
export type InputVariant = 'default' | 'error' | 'success';

/**
 * Component display variants
 */
export type DisplayVariant = 'default' | 'elevated' | 'outlined';

/**
 * Orientation variants
 */
export type OrientationVariant = 'horizontal' | 'vertical';

